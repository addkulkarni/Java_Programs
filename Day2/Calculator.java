public class Calculator
{
	public void add()
	{
		System.out.println(50+50);
		System.out.println("Addition of two numbers is " + (50+50));
	}

	public void sub()
	{
		System.out.println(50-20);
		System.out.println("Subtraction of two numbers is " + (50-20));
	}

	public void mul()
	{
		System.out.println(5*2);
		System.out.println("Multiplication of two numbers is " + (5*2));
	}

	public void div()
	{
		System.out.println(100/20);
		System.out.println("Division of two numbers is " + (100/20));
	}

	
		
}