public class VowelConsonant
{
	public static void main(String args[])
	{
		char letter = 'r';
		if(letter == 'a' || letter == 'e'|| letter == 'i'|| letter=='o'|| letter=='u')
		{
			System.out.println("Character is a Vowel");
		} 
		else
		{
			System.out.println("Character is a consonant");
		}
	}
}