public class Person
{
	int srno;
	String name;
	String address;
	int age;
	int weight;
	int height;
	long mobno;
	String profession;
	
	public Person(int srno, String name, String address, int age, int weight, int height, long mobno, String profession)
	{
		this.srno=srno;
		this.name=name;
		this.address=address;
		this.age=age;
		this.weight=weight;
		this.height=height;
		this.mobno=mobno;
		this.profession=profession;
	}
	
}