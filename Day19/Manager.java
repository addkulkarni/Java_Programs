public class Manager
{
	int id;
	String name;
	int salary;
	String qualification;
	String branchName;
	
	public Manager(int id, String name, int salary,	String qualification, String branchName)
	{
		this.id=id;
		this.name=name;
		this.salary=salary;
		this.qualification=qualification;
		this.branchName=branchName;
	}
}