public class LadderIf
{
	public static void main(String args[])
	{
		int i = 10;
		if(i==1)
		{
			System.out.println("Value is 1");	
		}
		else if(i==5)
		{
			System.out.println("Value is 5");	
		}
		else if(i==10)
		{
			System.out.println("Value is 10");	
		}
		else
		{
			System.out.println("Value not found");
		}	
	}
}