public class Calculator
{
	int i=10;
	int j=5;
	public void add()
	{
		int result=i+j;
		System.out.println(result);		
	}
		
	public void sub()
	{
		int result=i-j;
		System.out.println(result);
	}

	public void mult()
	{
		int result=i*j;
		System.out.println(result);
	}

	public void div()
	{
		int result=i/j;
		System.out.println(result);
	}
}