public class Person
{
	private String pname;
	private String paddr;
	private long pmob;
	
	public void setPname(String pname)
	{
		this.pname=pname;
	}
	public void setPaddr(String paddr)
	{
		this.paddr=paddr;
	}
	public void setPmob(long pmob)
	{
		this.pmob=pmob;
	}
	
	public String getPname()
	{
		return pname;
	}
	public String getPaddr()
	{
		return paddr;
	}
	public long getPmob()
	{
		return pmob;
	}
	
	
	
}

