public class University
{
	int uid;
	String uname;
	Address a;
	
	public void setUid(int uid)
	{
		this.uid=uid;
	}
	public void setUname(String uname)
	{
		this.uname=uname;
	}
	public void setA(Address a)
	{
		this.a=a;
	}
	
	public int getUid()
	{
		return uid;
	}
	public String getUname()
	{
		return uname;
	}
	public Address getA()
	{
		return a;
	}
}

